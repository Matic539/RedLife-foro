const mongoose = require('mongoose');

// Definición del esquema de post
const postSchema = new mongoose.Schema({
    title: {
        type: String,
        required: true
    },
    content: {
        type: String,
        required: true
    },
    author: {
        type: mongoose.Schema.Types.ObjectId,
        ref: 'User',
        required: true
    },
    authorName: { // Campo adicional para almacenar el nombre de usuario del autor
        type: String,
        required: true
    },
    createdAt: {
        type: Date,
        default: Date.now
    },
    updatedAt: {
        type: Date,
        default: Date.now
    }
});

// Creación del modelo de post
const Post = mongoose.model('Post', postSchema);

module.exports = Post;
